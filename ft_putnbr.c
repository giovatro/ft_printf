#include "ft_printf.h"

int	ft_putnbr(int n, int len)
{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
			len += 11;
		}
		else
		{
			ft_putchar(45, len);
			n = -n;
		}
	}
	else
	{
		if (n > 9)
			ft_putnbr(n / 10, len);
		ft_putchar((n % 10) + 48, len);
	}
	return (len);
}
